// Compare Lengths of Two Strings Using length
// Write a program to compare the lengths of two strings and 
// print which one is longer using the length property.
// Example:
// Input: "JavaScript", "Python"
// Output: "JavaScript" is longer than "Python".

let a="JavaScript";
let b="Python";
let c=a.length;
let d=b.length;

if(c>d){
    console.log("JavaScript is longer than Python");
}
else {
    console.log("JavaScript is lesser than Python");
}
